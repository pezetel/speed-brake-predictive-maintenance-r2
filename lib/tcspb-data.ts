export function getTCSPBData(): FlightRecord[] {
  return rawTCSPB.map(r => {
    const [date, tail, from, to, pfd, durD, durE, deg1, deg99, d30, d50, gs] = r;
    const aircraftType = detectAircraftType(tail);
    const isDoubled = pfd > 150;
    const normalizedPfd = isDoubled ? pfd / Math.round(pfd / 100) : pfd;
    const durationRatio = durD > 0 ? durE / durD : 0;
    const landingDistAnomaly = d50 > d30 * 1.05;

    const partial = {
      flightDate: parseDate(date),
      tailNumber: tail,
      takeoffAirport: from,
      landingAirport: to,
      pfdTurn1: pfd,
      durationDerivative: durD,
      durationExtTo99: durE,
      pfdTurn1Deg: deg1,
      pfeTo99Deg: deg99,
      landingDist30kn: d30,
      landingDist50kn: d50,
      gsAtAutoSbop: gs,
      aircraftType,
      isDoubledRecord: isDoubled,
      normalizedPfd,
      durationRatio,
      landingDistAnomaly,
    };

    const { level, reasons } = detectAnomaly(partial);
    return { ...partial, anomalyLevel: level, anomalyReasons: reasons };
  });
}

function parseDate(s: string): string {
  const parts = s.split('.');
  if (parts.length === 3) {
    const day = parts[0].padStart(2, '0');
    const month = parts[1].padStart(2, '0');
    const year = parts[2].length === 2 ? '20' + parts[2] : parts[2];
    return `${year}-${month}-${day}`;
  }
  return s;
}
